package SessionThree;

public class Loops {
    public static void main(String[] args) {  //for - while - do while
        //initial        ;           condition          ;         I/D
        for(int i=1 ; i <= 100 ; i++)
            System.out.println(i);

        int j = 200;
        while (j<=100)
        {
            System.out.println(j);
            j++;
        }



        int k =1;
        do {
            System.out.println(1);
            k++;
        }while (k <= 5);  //F



    }
}
